# What is enviroment?
# Enviroment is a place where we can run our code. It can be a computer, a server, a virtual machine, a container, etc.

# Examples of enviroments:
# 1. Local Enviroment
# 2. Development Enviroment
# 3. Testing Enviroment
# 4. Staging Enviroment
# 5. Production Enviroment

# How to create a virtual enviroment in python?
# 1. Open the command prompt
# 2. Type pip install virtualenv
# 3. Press Enter
# 4. Type python -m virtualenv myenv
# 5. Press Enter
# 6. Type myenv\Scripts\activate
# to deactivate the virtual enviroment, type deactivate


# How to install a package in a virtual enviroment?
# 1. Open the command prompt
# 2. Type pip install package_name
# 3. Press Enter
