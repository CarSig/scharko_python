# How to install python?

# # 1. Go to the official website of Python: https://www.python.org/
# # 2. Click on the Downloads tab
# # 3. Click on the latest version of Python
# # 4. Download the installer
# # 5. Run the installer
# # 6. Click on the Add Python to PATH checkbox
# # 7. Click on the Install Now button
# # 8. Wait for the installation to complete
# # 9. Click on the Close button
# # 10. Open the command prompt
# # 11. Type python --version
# # 12. Press Enter
# # 13. Check if the version of Python is displayed

# # If you are using a Mac, then Python is pre-installed on your system. You can check the version of Python by opening the terminal and typing python --version.

# # If you are using Linux, then you can install Python using the package manager. For example, on Ubuntu, you can install Python by typing sudo apt-get install python3 in the terminal.

# # If you are using Windows, then you can install Python using the installer. You can download the installer from the official website of Python and run the installer. Make sure to check the Add Python to PATH checkbox during the installation process.
