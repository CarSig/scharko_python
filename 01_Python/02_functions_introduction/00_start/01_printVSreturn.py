# print vs return
# print() is a function that prints the specified message to the screen, or other standard output device.
# return statement is used to exit a function and provide value 

def printVsReturn():
    print("This is printed")
    return "This is returned"

printVsReturn()  # Output: This is printed
print(printVsReturn())  # Output: This is printed
                        #         This is returned


