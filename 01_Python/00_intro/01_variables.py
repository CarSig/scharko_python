# what are variables?
# Variables are used to store data values. A variable is a name that refers to a value.
#
# Variable Names:
# Variable names can contain letters, numbers, and underscores.
# Variable names must start with a letter or an underscore.
# Variable names are case-sensitive.
# Variable names cannot contain special characters or spaces.
#
# Assigning Values to Variables:
# Variables are assigned values using the assignment operator =.
# The value of a variable can be changed by assigning a new value to it.

# Assigning a value to a variable
x = 5

# Accessing the value of a variable
print(x)  # Output: 5

# Assigning a new value to the variable
x = 10

# Accessing the new value of the variable
print(x)  # Output: 10

