# Sets
# Sets are unordered collections of unique elements. Duplicates are not allowed in sets. Sets are created using curly braces.

# Creating a set
example_set = {1, 2, 3, 4, 5}
# Adding elements
example_set.add(6)
# Removing elements

# This will throw an error if the element is not present
# example_set.remove(6)
# Looping through a set
for element in example_set:
    print(element)