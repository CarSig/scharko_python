# Lists

# Lists are ordered collections of elements. They can contain any type of data, including other lists.

# Creating a list
example_list = [1, 2, 3, 4, 5]

# Accessing elements
first_element = example_list[0]

# Slicing
sliced_list = example_list[1:3]

# Modifying elements
example_list[0] = 10

# Adding elements
example_list.append(6)

# Removing elements
example_list.remove(6)

# Looping through a list
for element in example_list:
    print(element)
