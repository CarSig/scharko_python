# what is request and response in flask
#
# The request object is used to access incoming request data. The response object is used to send data back to the client.


# what is session in flask
# The sessions object is used to store information specific to a user from one request to the next.


# what is the use of __name__ in flask
# The __name__ variable is a convenient way to tell Flask where to look for templates, static files, and so on.


# what is the use of app.run() in flask
# The app.run() method is called to start the development server. The __name__ variable is set to "__main__" to ensure that the development server is started only when the script is executed directly, not when it is imported as a module. The development server is started on the default port 5000.


# what is the use of route() decorator in flask
# The route() decorator is used to specify the URL that should trigger the execution of the hello() function. The hello() function returns the string "Hello, World!" when the specified URL is accessed.


# what is the use of request.get_json() in flask
# request.get_json() is used to get data from the request payload.
